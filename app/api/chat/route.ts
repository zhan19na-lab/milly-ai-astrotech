function pickResponse(message: string): MillyResponse {
  const lower = message.toLowerCase();
  for (const { keywords, response } of TEMPLATES) {
    if (keywords.some((kw) => lower.includes(kw))) return response;
  }
  return DEFAULT_RESPONSE;
}

